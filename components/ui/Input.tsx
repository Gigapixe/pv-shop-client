"use client";
import React, { useState } from "react";
import SearchIcon from "@/public/icons/SearchIcon";

interface InputProps extends React.InputHTMLAttributes<HTMLInputElement> {
  id?: string;
  label?: string;
  error?: string;
  className?: string;
  disabled?: boolean;
}

const Input: React.FC<InputProps> = ({
  id,
  label,
  error,
  className = "",
  disabled = false,
  type = "text",
  min,
  ...restProps
}) => {
  const [showPassword, setShowPassword] = useState(false);
  const [internalError, setInternalError] = useState<string | undefined>(error);

  // Determine input type for password visibility
  const inputType = type === "password" && showPassword ? "text" : type;

  const togglePasswordVisibility = () => {
    setShowPassword((prev) => !prev);
  };

  // Handle input validation for number type
  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    if (type === "number") {
      const value = e.target.value;
      // Allow empty string
      if (value === "") {
        setInternalError(undefined);
        restProps.onChange?.(e);
        return;
      }
      const numValue = Number(value);
      // Validate minimum value
      if (min && numValue < Number(min)) {
        setInternalError(`Value must be at least ${min}`);
      } else {
        setInternalError(undefined);
      }
    }
    restProps.onChange?.(e);
  };

  return (
    <div className="space-y-2">
      {label && (
        <label
          {...(id ? { htmlFor: id } : {})}
          className="block text-sm font-medium text-text dark:text-text-light"
        >
          {label}
        </label>
      )}
      <div className="relative">
        {type === "search" && (
          <div className="absolute inset-y-0 left-0 flex items-center pl-3 pointer-events-none">
            <SearchIcon className="w-4 h-4 text-gray-400 dark:text-gray-500" />
          </div>
        )}
        <input
          {...(id ? { id } : {})}
          type={inputType}
          className={`
            w-full px-3 py-2 border rounded-full 
            ${type === "search" ? "pl-10" : ""}
            border-border-light dark:border-border-dark
            bg-background dark:bg-background-dark
            text-text dark:text-text-light
            focus:outline-none focus:ring-2 focus:ring-primary 
            dark:focus:ring-primary-dark
            ${disabled ? "cursor-not-allowed opacity-60" : ""}
            ${
              internalError || error
                ? "border-red-500 dark:border-red-400 focus:ring-1 focus:ring-red-500 dark:focus:ring-red-400"
                : ""
            }          
            ${className}
          `}
          disabled={disabled}
          min={min}
          {...restProps}
          onChange={handleChange}
        />
        {type === "password" && (
          <button
            type="button"
            onClick={togglePasswordVisibility}
            className="absolute inset-y-0 right-0 flex items-center pr-3"
            disabled={disabled}
            tabIndex={-1}
          >
            <svg
              className={`h-5 w-5 text-gray-500 dark:text-gray-400 ${
                disabled ? "opacity-60" : ""
              }`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
              xmlns="http://www.w3.org/2000/svg"
            >
              {showPassword ? (
                <>
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M15 12a3 3 0 11-6 0 3 3 0 016 0z"
                  />
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z"
                  />
                </>
              ) : (
                <>
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21"
                  />
                </>
              )}
            </svg>
          </button>
        )}
      </div>
      {(internalError || error) && (
        <p className="text-red-500 text-sm">{internalError || error}</p>
      )}
    </div>
  );
};

export default Input;
